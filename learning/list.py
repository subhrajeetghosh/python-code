# list use to store multiple item in single variable

car = ["farrari", "audi", "bmw", "suzuki"]
print(car)
car[0] = "tata"
print(car)
car.append("tesla")
print(car)
car.remove("tata")
print(car)
car.pop()
print(car)
car.insert(0, "ram")
print(car)
car.sort()
print(car)
car.clear()
print(car)

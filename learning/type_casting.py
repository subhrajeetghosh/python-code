# Convert the data type of value to another data type value is called type casting

i = 2  # integer
j = 4.0  # float
z = "3"  # string

# converting i to string
print(str(i))
# converting j to integer
print(int(j))
# converting z to float
print(float(z) * 3)  # now after converting that you can do mathmatical operations

# you can do this vice versa with one another

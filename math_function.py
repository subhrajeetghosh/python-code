import math

pi = 3.14

a = 1
x = 4
y = 9
z = 10

print(round(pi))  # round the numeric variable to nearest integer
print(math.ceil(pi))  # round the numeric variable up to the nearest integer
print(math.floor(pi))  # round the numeric variable down to the nearest integer
print(abs(pi))  # absolute value of the numeric variable
print(pow(pi, 2))  # squering the numeric variable
print(math.sqrt(9))  # square root of numeric variable
print(max(a, x, y, z))  # finding the max number of 2 to n number of numeric variable
print(min(a, x, y, z))  # finding the min number of 2 to n number of numeric variable
